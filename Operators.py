#Arithmetic operators

x =40
y =32
print(x+y)
print(x-y)
print(x*y)
print(x/y)
print(x % y) # modulus= remainder


#Comparison operators
print(y<x)
print(y>x)
print(y<= x)
print(y>= x)
print(y== x)
print(y!= x)


#Assignment operator
a=10
print(a)

a += 2
print(a)

a *= 3
print(a)

#Logical operators- and,or,not
print(34<50 and 56>300)
print(34<50 or 56>300)
print(not(34<50 and 56>300))

